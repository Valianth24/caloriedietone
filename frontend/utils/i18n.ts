// Dil değiştirme fonksiyonu
export const changeLanguage = async (langCode: string) => {
  await AsyncStorage.setItem('app_language', langCode);
  await i18n.changeLanguage(langCode);
};

// Kayıtlı dili yükle
export const loadSavedLanguage = async () => {
  try {
    const savedLang = await AsyncStorage.getItem('app_language');
    if (savedLang) {
      await i18n.changeLanguage(savedLang);
      return savedLang;
    }
    return null;
  } catch (error) {
    console.error('Error loading saved language:', error);
    return null;
  }
};

// İlk giriş kontrolü
export const isFirstLaunch = async () => {
  try {
    const hasLaunched = await AsyncStorage.getItem('has_launched');
    if (hasLaunched === null) {
      return true;
    }
    return false;
  } catch (error) {
    return false;
  }
};

export const setFirstLaunchDone = async () => {
  await AsyncStorage.setItem('has_launched', 'true');
};

// Mevcut dili al
export const getCurrentLanguage = () => {
  return i18n.language || 'en';
};

// Dil bilgisini al
export const getLanguageInfo = (langCode: string) => {
  return languageList.find(l => l.code === langCode);
};

